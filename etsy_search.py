def get_titles(browser, page_limit):
    titles=[]
    for pageNum in range(0,page_limit):
        url = "https://www.etsy.com/ca/market/nails?ref=pagination&page="+str(pageNum+1)

        
        browser.get(url)
        title_elements=browser.find_elements(By.XPATH, "//*[starts-with(@id, 'listing-title')]")
        for title in title_elements:
            # print("title is: ", title.text)
            titles.append(title.text)
    return titles

def save_titles(filename, titles):
    with open("./"+filename, 'w') as file:
            file.write(json.dumps(titles))

def analyze(titles):
    token=re.findall('\w+', ''.join(titles))
    words=[]
    for word in token:
         if not word.lower() in stopwords:
              words.append(word.lower())
    dic = {}
    for word in set(words):
        dic[word] = words.count(word)
    aux = [(dic[key], key) for key in dic]
    aux.sort()
    aux.reverse()
    for pair in aux:
         print(pair)

